angular.module("leccion3App", []).controller("actividad1Ctrl", function($scope, $log) {
    "use strict";
    var shuffleArray = function(array) {
            var m = array.length,
                t, i;

            // While there remain elements to shuffle
            while (m) {
                // Pick a remaining element…
                i = Math.floor(Math.random() * m--);

                // And swap it with the current element.
                t = array[m];
                array[m] = array[i];
                array[i] = t;
            }

            return array;
        },
        getPreguntas = function() {
            return shuffleArray([{
                texto: "Los objetivos de investigación definen el propósito de mi investigación, y responden a las siguientes preguntas:",
                respuestas: [{
                    texto: "¿Qué quiero hacer?",
                    correcta: false
                }, {
                    texto: "¿Cuál es el fin de mi investigación?",
                    correcta: false
                }, {
                    texto: "¿Para qué la voy a hacer?",
                    correcta: false
                },{
                    texto: "Todas las anteriores",
                    correcta: true
                }]
            }, {
                texto: "Las siguientes preguntas le ayudan a establecer el QUÉ pretende su investigación:",
                respuestas: [{
                    texto: "¿Pretende contribuir a la solución de un problema en especial?",
                    correcta: false
                }, {
                    texto: "¿Pretende probar una teoría?",
                    correcta: false
                }, {
                    texto: "¿Aporta nuevos hallazgos en favor de una teoría en particular?",
                    correcta: false
                }, {
                    texto: "Todas las anteriores",
                    correcta: true
                }]
            }]);
        };
    $scope.preguntas = getPreguntas();

    $scope.validado = false;
    $scope.seleccionar = function(index, respuesta) {
        $scope.preguntas[index].respuesta = respuesta;
    };
    $scope.calificar = function() {
        var puntaje = 0,
            pregunta = null,
            i = 0;
        for (i = 0; i < $scope.preguntas.length; i++) {
            pregunta = $scope.preguntas[i];
            puntaje += pregunta.respuesta && pregunta.respuesta.correcta ? 1 : 0;
        }
        $scope.validado = true;

        var modalID = "#calificacionModal";
        puntaje = (puntaje / $scope.preguntas.length) * 100;
        puntaje = puntaje.toFixed(0);
        var exito = false;
        var mensaje = "Inténtalo nuevamente."
        if (puntaje == 100) {
            exito = true;
            mensaje = "¡Felicitaciones!"
        }
        registrarActividad(puntaje);
        mostrarCalificacion(modalID, puntaje + '%', mensaje, exito, $scope.reset);
    };
    $scope.reset = function() {
        $scope.$apply(function() {
            var i = 0,
                pregunta = null;
            for (i = 0; i < $scope.preguntas.length; i++) {
                pregunta = $scope.preguntas[i];
                if (pregunta.respuesta) {
                    pregunta.respuesta.input = !pregunta.respuesta.correcta ? undefined : pregunta.respuesta.input;
                }
            }

        });
    };
});
